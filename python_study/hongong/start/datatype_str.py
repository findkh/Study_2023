# 자료형과 문자열
# 파이썬에서 자료 형식을 확인할 떄는 type() 함수 사용
print(type("안녕하세요")) #<class 'str'>
print(type(114)) #<class 'int'>

# 문자열 내부에 큰따옴표 넣기
print('"안녕하세요"라고 말했는데...') #"안녕하세요"라고 말했는데...

# 문자열 내부에 작은따옴표
print("'뭐라고?'라고 대답했다.") #'뭐라고?'라고 대답했다.

# 이스케이프 문자 사용
#-> 역슬래시 기호 함께 조합한다.
#\" -> 큰따옴표
#\' -> 작은 따옴표
#\n -> 줄바꿈
#\t -> 탭
#\\ -> 역슬래시
print("\"큰따옴표\"를 넣어보았다.") # "큰따옴표"를 넣어보았다.
print("\'작은 따옴표\'를 넣어보았다.") # '작은 따옴표'를 넣어보았다.
print("수달\n레고\n개발자")
print("수달\t레고\t개발자\t")
print("\\ \\ ")

