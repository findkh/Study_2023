# 문자열 연산자
# + 연산자로 문자열 연결 연산을 적용할 수있다.
# 문자열은 무조건 문자열끼리 + 연산자를 사용해서 연결해야 한다.
print("안녕", "하세요") #안녕 하세요
print("안녕" + "하세요") #안녕하세요
print("안녕하세요" + "!") #안녕하세요!
# print("안녕하세요" + 1) #TypeError: can only concatenate str (not "int") to str
print("안녕하세요" + "1") #안녕하세요1

# 문자열 반복 연산자
# 문자열을 숫자와 *연사자로 연결하면 문자열을 반복할 수 있다.
print("안녕하세요" *3)
print(3 * "안녕하세요")

# 문자 선택 연산자(인덱싱): []
# 파이썬은 제로 인덱스 언어로 인덱스가 0부터 시작한다.
print("안녕하세요"[0])
print("안녕하세요"[1])
print("안녕하세요"[2])
print("안녕하세요"[3])
print("안녕하세요"[4])

print("안녕하세요"[4])
print("안녕하세요"[3])
print("안녕하세요"[2])
print("안녕하세요"[1])
print("안녕하세요"[0])

# 문자열 범위 선택 연산자(슬라이싱): [:]
print("안녕하세요"[1:3]) #녕하

# 문자열 범위 선택 연산자는 대괄호 안에 넣는 숫자 둘 중 하나를 생략하여 사용 가능
print("안녕하세요"[1:])
print("안녕하세요"[:3])

